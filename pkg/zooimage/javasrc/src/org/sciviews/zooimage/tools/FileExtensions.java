package org.sciviews.zooimage.tools;

/**
 * Contains File extensions used in ZooImage
 */
public class FileExtensions {

	/**
	 * The PhytoImage metadata file extension
	 */
	public static final String extZIM = ".zim"; 
	
	/**
	 * Extension used for initial images
	 */
	public static final String extTIF = ".tif";
	
	/**
	 * Extension used for initial images
	 */
	public static final String extJPG = ".jpg";
	
	/**
	 * The zipped RAW image
	 */
	public static final String extRAW = ".zip";
	
	/**
	 * The table of measurements
	 */
	public static String extDAT = "_dat1.zim";
	
	/**
	 * The mask file
	 */
	public static String extMSK = "_msk1.gif";
	
	/**
	 * The optical density calibrated image
	 */
	public static String extODC = "_odc1.gif";
	
	/**
	 * The outline and labels image
	 */
	public static String extOUT = "_out1.gif";
	
	/**
	 * The visual image
	 */
	public static String extVIS = "_vis1.gif"; 
	
	
}
